// Parse a scorecard xlsx and emit an idempotent SQL upsert into
// scorecard_snapshots. Mirrors lib/scorecard-parser.ts (matches KPIs by NAME,
// resolves house_id/kpi_id in SQL by slug/name). Usage:
//   node scripts/seed-scorecard.mjs <period> <xlsxPath>
import { createRequire } from 'node:module';
import { fileURLToPath } from 'node:url';
import path from 'node:path';
import fs from 'node:fs';

const require = createRequire(import.meta.url);
const XLSX = require('xlsx');
const __dirname = path.dirname(fileURLToPath(import.meta.url));

const period = process.argv[2] || 'P8';
const xlsxPath = process.argv[3] || path.join(__dirname, '../data/scorecard-p8.xlsx');

// Site Comparison house SCORE columns (0-based): D/F/H/J/L/N.
const HOUSE_COLUMNS = { maseeh: 3, mccormick: 5, baker: 7, next: 9, simmons: 11, 'new-vassar': 13 };

const wb = XLSX.readFile(xlsxPath);
const sheet = wb.Sheets['Site Comparison'];
if (!sheet) throw new Error('Missing "Site Comparison" sheet');
const rows = XLSX.utils.sheet_to_json(sheet, { header: 1, blankrows: false, defval: '' });

const esc = (s) => String(s).replace(/'/g, "''");
const values = [];
let blanks = 0;
let nonzero = 0;
for (const row of rows) {
  const kpiName = String(row[1] ?? '').trim();
  if (!kpiName || kpiName.toLowerCase() === 'kpi') continue; // header / TOTAL / PERFECT
  const category = String(row[0] ?? '').trim();
  if (!category || /total|perfect/i.test(category)) continue;
  for (const [slug, col] of Object.entries(HOUSE_COLUMNS)) {
    const raw = row[col];
    const blank = raw === '' || raw == null;
    if (blank) blanks++;
    const score = blank ? 0 : Number(raw);
    if (!blank && score !== 0) nonzero++;
    const s = Number.isFinite(score) ? score : 0;
    values.push(`  ('${period}', '${slug}', '${esc(kpiName)}', ${s})`);
  }
}

const sql = `-- ${period} scorecard seed (generated by scripts/seed-scorecard.mjs)
insert into scorecard_snapshots (period, house_id, kpi_id, score_0_3, weighted)
select v.period, h.id, k.id, v.score, 0
from (values
${values.join(',\n')}
) as v(period, slug, kpi_name, score)
join houses h on h.slug = v.slug
join kpi_definitions k on k.name = v.kpi_name
on conflict (period, house_id, kpi_id) do update set score_0_3 = excluded.score_0_3, weighted = 0, captured_at = now();
`;

const outFile = path.join(__dirname, `../data/seed-${period}.sql`);
fs.writeFileSync(outFile, sql);
console.error(
  `period=${period}  rows=${values.length}  (expected 120 = 6 houses x 20 KPIs)  blanks=${blanks}  nonzero=${nonzero}\nwrote ${outFile}`
);
